import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# --- ?????????? ---
INPUT_CSV = "grna_results.csv"

# ??????? ?????? (Publication Quality)
sns.set_theme(style="whitegrid")
plt.rcParams['figure.dpi'] = 300
plt.rcParams['savefig.dpi'] = 300
plt.rcParams['font.family'] = 'DejaVu Sans' # ????? ???? ??????? ????, ????? ?????? ???

# ???? ??? ???
try:
    df = pd.read_csv(INPUT_CSV)
    print(f"? ???? ??? ?????: {len(df)} ?? gRNA")
    
    # ?????? ??? ??? ??? (??????? ?? ????)
    # ????? ????? ??? ???????? ??? ?????? ????? ???? ????
    print(f"?????? ????: {df.columns.tolist()}")

except FileNotFoundError:
    print(f"? {INPUT_CSV} ?????? ????? ??????")
    exit()

# --- ????? ?: On-Target Efficiency Distribution ---
# ??? ?????? ????? ???????????? ?????????????
plt.figure(figsize=(8, 5))
sns.histplot(df['on_target_score'], bins=15, kde=True, color='#3498db')
plt.title('Distribution of gRNA On-Target Efficiency Scores', fontsize=14, fontweight='bold')
plt.xlabel('On-Target Score (0-100)', fontsize=12)
plt.ylabel('Count of gRNAs', fontsize=12)
plt.tight_layout()
plt.savefig('graph_efficiency_distribution.png')
plt.savefig('graph_efficiency_distribution.pdf')
print("?? ????? ? ??? ?????: Efficiency Distribution")
plt.close()

# --- ????? ?: Top 10 gRNAs based on Combined Score ---
# ???? ?????????? ??? ????? ????? ??????
top_10 = df.nlargest(10, 'combined_score')

plt.figure(figsize=(10, 6))
sns.barplot(x='combined_score', y='sequence', data=top_10, palette='viridis')
plt.title('Top 10 Candidate gRNAs (Combined Score)', fontsize=14, fontweight='bold')
plt.xlabel('Combined Score (Efficiency + Specificity)', fontsize=12)
plt.ylabel('gRNA Sequence (20bp)', fontsize=12)
plt.tight_layout()
plt.savefig('graph_top_10_grnas.png')
plt.savefig('graph_top_10_grnas.pdf')
print("?? ????? ? ??? ?????: Top 10 gRNAs")
plt.close()

# --- ????? ?: Position vs On-Target Score (Scatter Plot) ---
# ????? ??? ?????? ???? ???? ????? ????
plt.figure(figsize=(12, 6))
sns.scatterplot(data=df, x='position', y='on_target_score', 
                hue='on_target_score', size='on_target_score', 
                sizes=(20, 200), palette='RdYlGn', alpha=0.7)
plt.title('gRNA Efficiency across Gene Position', fontsize=14, fontweight='bold')
plt.xlabel('Position in Gene (bp)', fontsize=12)
plt.ylabel('On-Target Score', fontsize=12)
plt.legend(bbox_to_anchor=(1.05, 1), loc=2, borderaxespad=0.)
plt.tight_layout()
plt.savefig('graph_position_vs_efficiency.png')
print("?? ????? ? ??? ?????: Position vs Efficiency")
plt.close()

